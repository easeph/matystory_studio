function single(
  id: string,
  prompt: string,
  options: string[],
  answer: string,
  explanation: string,
  easterEgg: string
): QuizQuestion {
  return { id, type: "single", prompt, options, answer, explanation, easterEgg };
}

function blank(
  id: string,
  prompt: string,
  answer: string,
  explanation: string,
  easterEgg: string
): QuizQuestion {
  return { id, type: "blank", prompt, answer, explanation, easterEgg };
}

function judge(
  id: string,
  prompt: string,
  answer: boolean,
  explanation: string,
  easterEgg: string
): QuizQuestion {
  return { id, type: "judge", prompt, answer, explanation, easterEgg };
}

function buildQuiz(
  knowledgeSlug: string,
  questions: QuizQuestion[]
): QuizSet {
  return { knowledgeSlug, questions };
}

